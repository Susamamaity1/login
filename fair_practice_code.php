                                <h6 style="margin-top:2%;">4. KEY COMMITMENTS</h6><br>
                                <p>The Company commits to achieve the following with the implementation of this Code:<br>
                                i.	The Company commits itself to full customer satisfaction through efficient, professional and courteous services across all its branches.<br>
ii.	The Company shall consistently strive to meet with and improve upon the internally set benchmarks and practices and be ahead of the standards prevalent in the industry.<br>

iii.	The Company undertakes not to discriminate against customers on grounds of religion, caste, gender, sex or language.<br>
iv.	The Company will provide clear and full information about its products and services to its customers / prospective customers and will not resort to any misleading or potentially misguiding advertisement or publicity.<br>

v.	The Company undertakes to desist from introducing any products/services having elements of ‘hidden charges’ or lack of transparency.<br>

vi.	The Company will communicate in the local or vernacular language with the customer and in English at the request of the customer.<br>

vii.	The Company undertakes to take all possible and reasonable measures to secure the safe custody of the security pledged by the customer and to compensate the customer for any accidental, inadvertent or fraudulent loss of the security whilst in the custody of the Company.<br>

viii.	The company commits to act fairly and reasonably in its dealings with its borrowers by <br>
a)	Meeting the broad requirements and standards specified in the code for the products, services, offered and procedures and practices followed by employees.<br>
b)	Make sure that products and services are in accordance with relevant laws and regulations as applicable for the time being in force;<br>
c)	Make sure that company dealings with its borrowers are based on ethical principles of honesty, integrity and transparency.<br>

ix.	To assist its customers in understanding the broad features of its financial products and services, along with the inherent benefits and risks involved in availing the same.<br>
x.	To provide a trouble-free experience to its customers while dealing with it.<br>
xi.	To handle any errors or customer grievances promptly and effectively.<br>
xii.	The Company shall display the FPC on its website and also make available a copy of FPC on request of the Customer.

                                </p>
                                
    <h6 style="margin-top:2%;">5. KEY ELEMENTS ON FAIR PRACTICES CODE.</h6><br>
    <p>The FPC will be applicable to the following broad areas:<br>
A.	Loan applications and processing thereof<br>
B.	Loan appraisal and terms/conditions<br>
C.	Disbursement of loans including changes, if any, in terms and condition.<br>
D.	Post disbursement supervision/monitoring<br>
E.	Other general provisions<br>
F.	Confidentiality of Information<br> 
G.	Language and Mode of Communicating Fair Practice Code<br>
H.	Regulation of Rate of Interest
 </p>
 
<h6 style="margin-top:2%;font-size:14px;">A.	Loan Applications and Their Processing</h6><br>
<p>i.	Loan application form (physical or digital) of the company will include necessary information which are likely to affects the interests of borrower, so that a meaningful comparison with the terms and conditions offered by other NBFCs and more informed decisions can be taken by the borrower for their financial requirements.<br> 
ii.	The loan application form shall also mention the documents required that an applicant will be required to submit or upload with their application form.<br>
iii.	 All loan products or services offered shall be the same as laid down in the Loan Policy of the company.<br>
iv.	All communications to the borrower shall be in the English language or vernacular language or a language as understood by the borrower.<br>
v.	The Company shall inform the prospective borrower about the entire loan lifecycle that includes the processes involved in loan sanction, disbursement and recovery.<br>
vi.	The Company shall also provide acknowledgment for receipt of all loan applications after receipt of all the required complete information and documents stipulated in application form and such acknowledgement shall also indicate the time frame within which the loan applications will be disposed of.<br>
vii.	All the loan applications shall be disposed of in reasonable time frame from the date of receipt of duly completed Loan Application Forms together with the requisite documents.
</p>

 <h6 style="margin-top:2%;font-size:14px;">B.	Loan Appraisal and Terms/Conditions</h6><br>
<p>
    i.	The Company will ensure that there is proper assessment of loan application made by borrowers. The assessment would be in line with Company’s credit policies and procedures.<br>
ii.	“FTAPL” shall appraise loan application keeping the risk-based approach, Credit Policy and relevant applicable guidelines and regulations in mind.<br>
iii.	The Company shall inform the borrower about the loan amount sanctioned, in writing in the vernacular language as understood by the borrower via a Sanction Letter or other Loan Documents along with the terms and conditions including the annualised rate of interest and method of application thereof.<br>
iv.	The Company shall keep the acceptance of such Loan terms and conditions by the borrower on its record.<br> 
v.	As a measure of ensuring transparency in its lending activities, the Company shall mention the penal interest charged for late repayment in bold in the any of the document namely loan agreement / transaction documents / sanction letter and/or mail communication sent for this purpose/ Mobile application/Charges section on the Company & products’ websites”.<br>
vi.	The Company shall also provide a copy of the Loan Agreement along with a copy each enclosure quoted in the Loan Agreement to each borrower at the time of sanction/disbursement of loan.<br>
vii.	If the Company rejects any loan application, it shall communicate the reason(s) for such rejection in writing to the applicant.

</p>
 
 
  <h6 style="margin-top:2%;font-size:14px;">C.	Loan Disbursement and Changes in Terms and Conditions</h6><br>
  <p>i.	The Company shall give due notice to the borrower in a vernacular language regarding any changes made by it to the terms and conditions including disbursement schedule, interest rates, service charges, prepayment charges, etc.<br>
ii.	Changes in the interest rates and charges shall be affected only prospectively. A provision with respect to the same shall also be included in the Loan Agreement.<br>
iii.	Any decision relating to recall or acceleration of payment of performance under the Loan Agreement shall be as per its stipulation in the loan agreement. Before taking any such decision, the Company shall give notice to customers as per the covenants of the loan agreement in the vernacular language as understood by the customer.<br>
iv.	The borrower shall enter into an agreement with the Company by providing consent via clickwrap / Digital signature / Electronic signature etc, to all standard and specific terms and conditions before seeking disbursement
</p>

  <h6 style="margin-top:2%;font-size:14px;">D.	Post disbursement supervision/monitoring</h6><br>
  <p>
      i.	The Company shall give reasonable time to the borrowers before recall the loan or asking for accelerating the payment or performance subject to the terms and conditions contained in the Loan Agreement and other related documents.<br>
ii.	The Company shall release all securities on repayment of its full dues or on the realization of the outstanding amount of loan subject to any legitimate right or lien for any other claim the Company may have against its borrowers. If such right of set-off is to be exercised, the borrower shall be given notice about the same with full particulars about the remaining claims and the conditions under which the Company is entitled to retain the securities till the relevant claim is settled/ paid.

  </p>
  
    <h6 style="margin-top:2%;font-size:14px;">E.	General Guidelines</h6><br>
    <p>i.	The Company shall ensure that it does not, in any manner, interfere in the borrower’s affairs, except for the purposes specified in the terms and conditions of the loan agreement, unless new information that was not disclosed by the borrower earlier comes to its notice.<br>
ii.	If the Company receives any request from the borrower regarding transfer of their borrower account to another NBFC, bank or financial institution, the Company shall convey its consent or otherwise i.e., rejection, to the borrower within 21 days of receipt of such request. Further, such transfer of borrower’s account shall be done as per the contractual terms of the loan and in accordance with the applicable statutes, rules, regulations and guidelines.<br>
iii.	In case the Company needs to initiate the loan recovery process against the borrower, it shall sensitise its staff to fair recovery practices and ensure that it does not resort to undue or harassing methods such as persistently bothering the borrowers at odd hours, using muscle power to recover the loan amount, etc.<br>
iv.	In the event of non-adherence to the repayment schedule by the borrower, the Company shall follow a clear process as per the applicable laws, rules and regulations in order to recover its dues. The process will begin with the Company sending a legal notice to the borrower, followed by personal visits to the borrower. The Company may further refer cases of default to reputed recovery agents and inform the customer of the recovery proceedings initiated against them.<br>
v.	The Company shall not charge foreclosure charges/pre-payment penalties on all floating rate term loans sanctioned to individual borrowers
</p>

    <h6 style="margin-top:2%;font-size:14px;">F.	Confidentiality of Information</h6><br>
    <p>i.	All the personal information of the Company’s customers shall be treated as private and confidential by the Company and all of its employees.<br> 
ii.	Except under the following circumstances, the Company shall not disclose any transaction detail of the borrower to any third party:<br>
i.	When the Company has an obligation under any statutory or regulatory laws to provide such information to any statutory body, law enforcement agency, CIBIL, the RBI or any other state, central or regulatory body, including courts and tribunals with jurisdiction. <br>
ii.	When the customer has given due consent to the Company to share such financial data.<br>
iii.	When sharing of such information will be in public interest
</p>

    <h6 style="margin-top:2%;font-size:14px;">G.	Language and Mode of Communicating Fair Practice Code</h6><br>
    <p>
        i.	The Company shall upload this Fair Practices Code in the vernacular language or a language as understood by the borrower on its website for the reference of its various stakeholders.<br>
ii.	The Company shall ensure that all additions or changes to this Code uphold the spirit of the RBI’s guidelines on Fair Practices Code.

    </p>

     <h6 style="margin-top:2%;font-size:14px;">H.	Regulation of Rate of Interest</h6><br>
     <p>i.	The Company’s Board shall ascertain an interest rate of each loan product considering assorted factors such as the cost of funds, risk premium, margin, industry trend, opex cost, etc., on the approval of the board.<br>
ii.	The Company has also drafted a separate “Interest Rate Policy” to lay down the principles and guidelines for determining its interest rates and other charges.<br>
iii.	The rate of interest shall be annualised rate as a measure to inform the borrower about the exact rates charged to their borrower account.
</p>

<h6 style="margin-top:2%;">7.  GRIEVANCE REDRESSAL MECHANISM</h6><br>
    <p>
        The Board of Directors of the Company shall be responsible to lay down a proper grievance redressal mechanism to handle the dispute arising out of the internal decision-making. The purpose of such a robust mechanism will be to ensure that all the disputes relating to the Company’s lending decisions and other functions are heard and disposed of promptly at least at the next higher level.<br>The company also lay down the proper grievance redressal mechanism to handle complaints/grievances of its borrowers/customers. <br>The Company shall display the information pertaining to its grievance redressal mechanism, details of its Grievance Redressal Officer and the information of the Regional Office of the RBI prominently at its websites, office/place of business for resolution of complaints of its customers.<br>Further, the Company has also laid down a comprehensive robust Grievance Redressal Mechanism for the speedy disposal/ remedy of its customers’ complaints/grievances. The same is available on the website of the company I.e. https://royalsec.in/grievance_redressal.php or has been adopted, and approved, by the Board in detail under the Grievance Redressal Policy.
    </p>
    
    <h6 style="margin-top:2%;">8. FORCE MAJEURE</h6><br>
    <p>The various commitments outlined and made by the company shall be applicable under the normal operating environment. In the event of any Force Majeure circumstances, the Company may not be able to fulfill the objectives under the FPC to the entire satisfaction of the borrowers, the stakeholders, and the public in general.</p>

    <h6 style="margin-top:2%;">9. REVIEW </h6><br>
    <p>The Board of Directors will annually review the compliance of this Fair Practices Code and the effectiveness of functioning of the Company’s Grievance Redressal Mechanism. The Board shall also make appropriate modifications in the FPC from time to time in conformity to the changes or additions to the guidelines as issued by the RBI.</p>
    
        <h6 style="margin-top:2%;">10. Disclaimer </h6><br>
        <p>The Company is abide by all guidelines, directives, instructions, and advice of the Reserve Bank of India as in force from time to time. The content in this document shall be read in conjunction with these guidelines, directives, instructions and advices. The Company will consistently apply better practice so long as such practice does not conflict with or violate Reserve Bank of India regulations.</p>


 
                                 

                                
                    
