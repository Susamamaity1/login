                    <h6>1.INTRODUCTION</h6>
                    <p>FASHION TRADES & AGENCIES PRIVATE LIMITED (hereinafter referred to as “the Company” or “FTAPL”) is a Non-Deposit taking Non-Systematically important Non- Banking Finance Company registered with the Reserve Bank of India (“RBI”) engaged in the business of facilitating personal Loans individual loans, MSME loans etc.<br>The Reserve Bank of India (RBI) has issued guidelines on Fair Practices Code for Non-Banking Financial Companies (NBFCs) through Circular no. DNBR (PD) CC.No.054/03.10.119/2015-16 dated July 01, 2015, and Chapter V of Master Direction - Non-Banking Financial Company – Non-Systemically Important Non-Deposit taking Company and Deposit taking Company (Reserve Bank) Directions, 2016 which laying down standards for fair business and corporate practices while dealing with their customers.<br>“FTAPL” hereby put in place the board-approved Fair Practices Code (“the FPC”) in conformity with the guidelines issued by the RBI. The Company shall also make appropriate modifications in the FPC from time to time to conform to the standards that may be prescribed by RBI from time to time. Considering the nature of the business of the Company.<br>This Fair Practices Code is aimed to provide all the stakeholders of “FTAPL” especially its customers, an effective overview of the practices that it will follow while extending its financial products and services. 
                    </p>
                    
                    
                 <h6 style="margin-top:2%;">2. PURPOSE </h6>
                 <p>The first and foremost purpose of this FPC is to promote good practices and ensure good practices in dealing with customers and ensure that the Company’s business is conducted in accordance with the prevailing statutory and regulatory requirements, with due focus on efficiency, customer-orientation and corporate governance principles.<br>The Company’s fair lending practices shall apply across all aspects of its operations including marketing, loan origination, processing, and servicing collection activities. <br>This Code shall further ensure that the Company’s financial products and services are available to all eligible loan applicants, without any bias or discrimination based on their race, caste, colour, religion, gender, marital status, age, etc. <br>The Company’s Board of Directors and the management are responsible for establishing practices designed to ensure that its operations reflect a strong commitment to fair lending and that all employees are aware of that commitment. 
                 </p>
                    
