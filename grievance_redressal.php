<h6 style="margin-top:2%;">7. GRIEVANCE REDRESSAL TIMEFRAME</h6><br>
<p>The timeframe for addressing and resolving a complaint shall differ on a case-to-case basis, and depend upon the type and complexity of the grievance. The timelines for redressal of different kinds of complaints under this Policy is as follows:<br>
• General cases (other than the cases mentioned below). These include customer complaints pertaining to business practices, lending decisions, credit management, recovery and complaints relating to updating/altering credit information, etc.: 15 working days of receipt of complaint.<br>
• Fraud cases, legal cases and cases which require retrieval of old records and documents: 30 working days of receipt of complaint.<br>
• CIBIL-related cases: 25 working days of receipt of complaint.<br>
• EMI Related issue Cases: 20 Working days from the date of receipt of complaint.
<br>
If any complaint needs additional time to reach a resolution, the Company will inform the complainant of the requirement of additional time and the expected timeline for the resolution of the issue.
</p>

