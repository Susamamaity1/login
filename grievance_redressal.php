<h6 style="margin-top:2%;">5. GROUND OF FILING COMPLIANTS</h6><br>
<p>Customer can file/register compliant(s) on the following grounds mentioned under clause 8 of Ombudsman Scheme for NBFCs, 2018 In writing containing the nature of grievance/deficiency, inter alia, with regard to:<br>
i.	Failure to convey in writing, the amount of loan sanctioned along with terms and conditions including annualised rate of interest and method of application thereof;<br> 
ii.	Failure or refusal to provide sanction letter/ terms and conditions of sanction in vernacular language or a language as understood by the borrower;<br>
iii.	Failure or refusal to provide adequate notice on proposed changes being made in sanctioned terms and conditions in vernacular language as understood by the borrower;<br>
iv.	Failure or inordinate delay in releasing the securities documents to the borrower on repayment of all dues; i) levying of charges without adequate prior notice to the borrower/ customer; j) failure to provide legally enforceable built-in repossession clause in the contract/ loan agreement;<br>
v.	Failure to ensure transparency in the contract/ loan agreement regarding (i) notice period before taking possession of security; (ii) circumstances under which the notice period can be waived; (iii) the procedure for taking possession of the security; (iv) a provision regarding final chance to be given to the borrower for repayment of loan before the sale/ auction of the security; (v) the procedure for giving repossession to the borrower and (vi) the procedure for sale/ auction of the security;<br> 
vi.	Non-observance of directions issued by Reserve Bank to the non-banking financial companies;<br> 
vii.	Non-adherence to any of the other provisions of Reserve Bank Guidelines on Fair Practices Code for Non-Banking Financial Companies.

</p>

