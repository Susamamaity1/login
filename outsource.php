                    <h6>1.	PREAMBLE</h6>
                    <p>This Policy shall be termed as Outsourcing Policy of FASHION TRADES & AGENCIES PRIVATE LIMITED (“The Company” or “FTAPL”) . The terms in this policy shall be considered as defined by the Reserve Bank of India in its Directions on Managing Risks and Code of Conduct in Outsourcing of Financial Services by NBFCs and other various directions, guidelines as issued and may be issued from time to time and, or as defined herein below. <br>Outsourcing involves transferring financial activities to the third party (either an affiliated entity within a corporate group or an entity that is external to the corporate group) to activities on a continuing basis that would normally be undertaken by the NBFC itself, now or in the future. Perform significantly.<br>The outsourcing of activities falls within the purview of guidelines of the Reserve Bank of India (“RBI”) on “Managing Risks and Code of Conduct in Outsourcing of Financial Services by NBFCs” issued on November 9, 2017 (Including amendment thereof) which requires the non-banking financial companies to formulate an outsourcing policy. In compliance with these RBI guidelines, this outsourcing policy (“Policy”) has been framed by the Company duly approved by its Board of Directors.<br>
                    Outsourcing always involves a considerable degree of two-way information exchange, coordination and trust. Outsourced financial services include applications processing (loan origination), document processing, marketing and research, supervision of loans, data processing and back office related activities etc. Outsourcing business is often characterized by expertise not inherent to the core of the client organization.<br>
                    The outsourcing of financial activities with in regulatory preview with an objective to:<br>
                    1.	Protect the interest of the customers of NBFCs, and<br>
2.	To ensure that the NBFC concerned and the Reserve Bank of India have access to all relevant books, records and information available with service provider. 

                    </p>
                    
                    
