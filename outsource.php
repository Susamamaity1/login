               <h6 style="margin-top:2%;">3.	Definitions</h6><br>
               <p>In this Policy, unless expressly defined otherwise in this Policy, the capitalized terms shall have the following meanings:<br>
 “Arrangement” means an agreement with a service provider wherein such service provider agrees/ promises to provide necessary services using its own staff and equipment, and usually at its own facilities;<br>“Board of Directors” or “Board” in relation to the Company, means the collective body of the Directors of the Company;<br>
 “Business-critical Processes” means the processes essential for carrying out operations of the Company which does not include its core management functions”<br>
 “Code of Conduct” means a set of rules outlining the rules and responsibilities of the Board, Senior Management, Direct Sales Agents (DSA), Direct Marketing Agents (DMA) and/or Recovery Agents, Lending service provider;<br>
 “Continuing basis” includes agreements for a limited period;<br>
 “Material Outsourcing” means such arrangements which, if disrupted, have the potential to significantly impact the business operations, reputation, profitability or customer service, and the materiality of outsourcing would be determined based on<br>
 •	the level of importance of the activity being outsourced, and significance of the risk posed by the same, to the Company; <br>
•	the potential impact of the outsourcing on the Company on various parameters such as earnings, solvency, liquidity, funding capital and risk profile; <br>
•	the likely impact on the Company’s reputation and brand value, and ability to achieve its business objectives, strategy and plans, should the Service Provider fail to perform the service;<br>
•	the cost of the outsourcing as a proportion of total operating costs of the Company; <br>
•	the aggregate exposure to that particular Service Provider, in cases where the Company outsources various functions to the same Service Provider; and <br>
•	the significance of activities outsourced in context of customer service and protection.<br>

“Outsourcing” means the Company’s use of a third party (either an affiliated entity within a Company group or an entity that is external to the Company group) to perform activities on a continuing basis that would normally be undertaken by the Company itself, now or in the future;<br>
“Senior Management” comprises of the Key Managerial Personnel of the Company, Business/Unit Heads and such other employees as authorized by the Company from time to time; <br>
“Service Level Agreement” or “Outsourcing Agreement” means a contract between a service provider (either internal or external) and the Company that defines the level, terms & conditions of service expected from the Service Provider.<br>
“Service Provider” means any third party (either an affiliated entity within FTAPL group or an entity that is external to FTAPL group) that performs business-critical services on the continuing basis (includes arrangements for a limited period) that would normally be undertaken by the Company itself, now or in the future. The services provided must be necessary for continuity of business processes and include, inter alia, the following: <br>
1. Services that aid in credit appraisal such as tele-verification, providing credit reports, field investigation, title search, etc.; 
2. Services that aid in customer file verification, storage and in-warding and resolution of customer queries; <br>
3. Services that aid in the collection of payments from the customers, legal services, repossession services, etc; <br>
4. IT services including both software (owned and as a service) and hardware; and <br>
5. Such other services which are essential to business continuity as per the Company from time to time, unless otherwise specified in this Policy.<br>

4.	<b>Role of the Company, Regulatory and Supervisory Requirements</b><br>
The Company, through the Senior Management or the respective Head of Departments, Business/Unit heads shall ensure that:<br>
1. Outsourcing arrangement of any activity by the Company does not diminish its obligations and/or ability to fulfill its obligations to customers and RBI, and those of its Board and Senior Management who have the ultimate responsibility for the outsourced activity. <br>
2. Outsourcing arrangement does not impede effective supervision of the RBI over such activities.  <br>
3. Outsourcing arrangement shall not affect the rights of a customer against the Company, including ability of the customer to obtain redressal of his/her grievance as per the provisions of this Policy or the applicable laws.  <br>
4. Ultimate control of the outsourced activity remains with the Company as it is responsible for confidentiality of the customers’ information available with its Service Providers and it may also be held responsible for the actions of its Service Providers including Direct Sales Agents, Direct Marketing Agents, Recovery Agents, and Lending Service provider;  <br>
5. Applicable provisions of the relevant laws, regulations, guidelines and conditions of approval, licensing and registration are considered while doing due diligence of the Service Provider in relation to outsourcing;  <br>
6. The Service Provider including its location, whether in India or abroad, shall not impede or interfere with the ability of the Company to effectively oversee and manage its activities and shall also not impede the RBI in carrying out its supervisory functions and objectives;  <br>
7. Clause shall be incorporated in the product literature/ brochures, etc., stating that the Company may use the services of agents in sales and marketing, etc. of the products, and if possible, role of such agents may be indicated in broad terms;  <br>
8. The Service Provider (other than Group Company) is not owned or controlled by any director of the Company or their relatives (as defined in the Companies Act, 2013).

