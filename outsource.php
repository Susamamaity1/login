                                <h6 style="margin-top:2%;">9.	Service Level Agreement / Outsourcing Agreement (SLA)</h6><br>
                                All Service Providers, prior to selection, must be given clarity on the level of service that the Company expects from them. The terms of the Service Level Agreement (“SLA”) shall be decided by the board and mutually agreed upon by the Service Provider. For Service Providers providing same or similar services, the terms of the SLA shall be identical to ensure equity and parity amongst the Service Providers. 
Post definition of the SLA, the Service Provider must demonstrate, through documentary evidence or otherwise, that it is capable of adhering to the norms put forth in the SLA. An acceptable breach ratio must also be mutually agreed upon by the Service Provider and the Company. The breach ratios for all services must be defined by the respective Head of Departments and approved by the Chief Operating Officer or Managing director or board of the Company.

                                <h6 style="margin-top:2%;">Every SLA shall include the following provisions:</h6><br>
                                1.	Nature of Legal relationship between the parties i.e.; whether agent, principal or otherwise;<br>

2.	What activities are going to be outsourced? (including appropriate service and its performance standards);<br>

3.	Determining the ability to access all books, records and information relevant to the outsourced activity available with the Service Provider; <br>

4.	Ability for continuous monitoring and assessment of the Service Provider by the Company so that any necessary corrective measure can be taken immediately; <br>

5.	Controls to ensure customer data confidentiality and the Service Provider' liability in case of breach of security and leakage of confidential customer related information; <br>

6.	there must be contingency plans to ensure business continuity; <br>

7.	Termination clause and minimum period to execute a termination provision (Notice Period);<br>


8.	Limited access of data to the employees of the Service Provider only on a “need to know” basis and availability of adequate checks and balances at the end of the Service Provider to ensure the same; <br>

9.	Requirement of prior approval/ consent from the Company for use of sub-contractors by the Service Provider for all or part of an outsourced activity and includes, where necessary, conditions of sub-contracting by the Service Provider in order to maintain a similar control over the risks by the Company;<br>

10.	Must have an confidentiality clause to ensure protection and confidentiality of customer data even after the SLA expires or gets terminated; <br>

11.	Provides for the Company with the right to conduct audits on the Service Provider whether by its internal or external auditors, or by agents appointed to act on its behalf and to obtain copies of any audit or review reports and findings made on the Service Provider in conjunction with the services performed for the Company; <br>

12.	Provides for the RBI or persons authorized by it to access the Company's documents, records of transactions, and other necessary information given to, stored or processed by the Service Provider within a reasonable time; <br>

13.	Provides for right of the RBI to cause an inspection to be made of a Service Provider of the Company and its books and account by one or more of its officers or employees or other persons; <br>

14.	Requirement of the Service Provider to preserve documents as required by law and take suitable steps to ensure that the Company’s interests are protected in this regard even post termination of the services.<br>

                                <h6 style="margin-top:2%;">10.	Comprehensive Risk Management Program to address the outsourced activities </h6><br>
                                <p>This Policy shall be communicated to all vertical/functional heads and other concerned persons of the Company who shall evaluate and guard against the following risks in outsourcing by the Company: <br>•	Strategic Risk – Where the Service Provider conducts business on its own behalf, inconsistent with the overall strategic goals of the Company. <br>
•	Reputation Risk – Where the service provided is poor and customer interaction is not consistent with the overall standards expected of the Company. <br>
•	Compliance Risk – Where privacy, consumer and prudential laws are not adequately complied with by the Service Provider. <br>
•	Operational Risk – Arising out of technology failure, fraud, error, inadequate financial capacity to fulfil obligations and / or to provide remedies. <br>
•	Legal Risk – Where the Company is subjected to fines, penalties, or punitive damages resulting from supervisory actions, as well as private settlements due to omissions and commissions of the Service Provider. <br>
•	Exit Strategy Risk – Where the Company is over-reliant on one firm, the loss of relevant skills in the Company itself preventing it from bringing the activity back in-house and where the Company has entered into contracts that make speedy exits prohibitively expensive. <br>
•	Counter party Risk – Where there is inappropriate underwriting or credit assessments. <br>
•	Contractual Risk – Where the Company may not have the ability to enforce the contract. <br>
•	Concentration and Systemic Risk – Where the overall industry has considerable exposure to one Service Provider and hence the Company may lack control over the Service Provider.<br> 
•	Country Risk – Due to the political, social or legal climate creating added risk. <br>
The risks and materiality of all the existing and prospective outsourcing shall be reviewed by the Head of Departments and by the Board/Committee (if required) from time to time as may be necessary. In the Outsourcing Agreement, the Company shall be provided with a right to conduct audits on the Service Provider whether by its internal or external auditors, or by agents appointed to act on its behalf and to obtain copies of any audit or review reports and findings made on the Service Provider in conjunction with the services performed for the Company.
</p>

