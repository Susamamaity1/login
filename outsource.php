                      <h6 style="margin-top:2%;">12.	Confidentiality and Security</h6><br>
Public confidence and customer trust in the Company are a pre-requisite for the stability and reputation, and therefore, the respective Head of the Departments shall ensure that<br>
1.	Outsourcing Arrangement shall ensure preservation and protection of the security and confidentiality of customer information in the custody or possession of the Service Provider;<br>
2.	Access of customer information to the staff of the Service Provider shall be on 'need to know' basis i.e. limited to those areas where information is required in order to perform the outsourced function; <br>
3.	The Service Provider shall isolate and clearly identify the Company’s customer information, documents, records and assets to protect the confidentiality of the information. In Instances, where the Service Provider acts as an outsourcing agent for multiple companies, care shall be taken to build strong safeguards so that there is no comingling of information/documents, records and assets; <br>
4.	Security practices and control processes of the Service Provider shall be reviewed and monitored on a regular basis and the Service Providers shall be required to disclose security breaches; <br>
5.	Any breach of security and leakage of confidential customer related information shall be notified to RBI.

                       <h6 style="margin-top:2%;">13.	Business Continuity and Management of Disaster Recovery Plan</h6><br>
                       <p>The Company, through the concerned Head of Departments, shall ensure that: <br>
1.	The Service Providers have developed and established a robust documented and tested framework for business continuity and recovery procedures which shall be reviewed on annual basis; <br>
2.	A notice period is incorporated in the Outsourcing Arrangement in order to mitigate the risk of unexpected termination thereof or liquidation of the Service Provider. To deal with such situation, an appropriate level of control and right to intervene shall be retained in the Outsourcing Arrangement with appropriate measures to continue the business operations of the Company without incurring prohibitive expenses and without any break in services to the customers of the Company; <br>
3.	Alternative Service Providers are available or there is a possibility of bringing the outsourced activity back in-house in case of emergency. <br>
4.	The Service Providers are able to isolate the Company’s information, documents and records, and other assets, and to ensure this, a clause may be incorporated in the Outsourcing Arrangement that after the termination of the contract, the Company can take back all the documents, records of transactions and information given to the Service Provider in order to continue its business operations, or otherwise delete, destroy or render unusable the same.
</p>

             <h6 style="margin-top:2%;">14.	Monitoring and Control of Outsourced Activities</h6><br>
             <p>The Head of Departments shall monitor and control the outsourcing activities of the Company and shall ensure that outsourcing agreements with the Service Provider contain provisions to monitor and control the outsourced activities. The Head of Departments shall meet on a half-yearly basis for the following purposes: <br>
1.	To review the central record of all Material Outsourcing maintained by the Company. The said records shall be updated promptly and half yearly reviews will be placed before the board or  Committee. <br>

2.	To review, on annual basis, the financial and operational condition of the Service Provider so as to assess its ability to continue to meet its outsourcing obligations. Such due diligence reviews, which will be based on all available information about the Service Provider will highlight any deterioration or breach in performance standards, confidentiality and security, and in business continuity preparedness. <br>
The respective Head of Departments shall ensure that: <br><br>
1. In the event of termination of the outsourcing agreement for any reason in cases where the Service Provider deals with the customers, the same shall be publicized by displaying at a prominent place in the branch, posting it on the web-site and informing the customers, so as to ensure that the customers do not continue to deal with the Service Provider. <br>
2. Reconciliation of transactions between the Company and the Service Provider (and/or its sub-contractor) are carried out in a timely manner in case of outsourcing arrangements requiring reconciliation of transactions, for example, outsourcing of cash management. An ageing analysis of entries pending reconciliation with the Service Providers shall be placed before the Committee of the Board and the efforts shall be made to reduce the old outstanding items therein at the earliest. <br>
3. A robust system of internal audit of all the outsourced activities is in place and monitored by the board/Committee of the Company. <br>
4. Regular audits are conducted by senior management of the Company to assess the adequacy of the risk management practices adopted in overseeing and managing the outsourcing arrangement, the Company’s compliance with its risk management framework and the requirements of the RBI guidelines.

</p>

                      <h6 style="margin-top:2%;">15.	Reporting of transactions to FIU or other competent authorities</h6><br>
                      <p>The respective Head of Departments shall provide the Currency Transactions Reports and Suspicious Transactions Reports to FIU or any other competent authority in pursuance of prevention of money laundering act read prevention of money laundering rules 2005 with the  in respect of the Company’s customer-related activities carried out by the Service Providers.</p>
                      
                      
                             <h6 style="margin-top:2%;">16.	Outsourcing within a Group/ Conglomerate</h6><br>
                             <p>In case of outsourcing of any activity within the group companies, the respective Head of Departments shall ensure that<br>
1.	Arm’s length distance is maintained in such outsourcing in terms of premises, manpower, decision-making, record keeping, etc. for avoidance of potential conflict of interests between the Company and such Service Provider and accordingly necessary disclosures in this regard shall be made as part of the outsourcing agreement;<br>
2.	The customers are informed specifically about the company which is actually offering the product/ service in case of involvement of multiple group entities involved or cross selling of products;<br>
3.	The outsourcing agreement shall address the provisions including scope of services, charges for the services and maintaining confidentiality of the customer's data; 
4.	The arrangement shall not lead to any confusion to the customers on whose products/ services they are availing by clear physical demarcation of the space where the activities of the Company and those of its other group entities are undertaken; <br>
5.	The arrangement do not compromise the ability to identify and manage risk of the Company on a stand-alone basis; <br>
6.	The arrangement do not prevent the RBI from being able to obtain information required for the supervision of the Company or pertaining to the group as a whole; <br>
7.	The outsourcing agreement must have a clause that there is a clear obligation for any service provider to comply with directions given by the RBI in relation to the activities of the Company; <br>
8.	Their ability to carry out their operations in a sound fashion would not be affected if premises or other services (such as IT systems, support staff) provided by the group entities become unavailable; <br>
9.	If the premises of the Company are shared with the group entities for the purpose of crossselling, the Company shall take measures to ensure that the entity's identification is distinctly visible and clear to the customers;<br>
10.	The marketing brochure used by the group entity and verbal communication by its staff / agent in the Company premises shall mention nature of arrangement of the entity with the Company so that the customers are clear about the seller of the product; <br>
11.	The Company shall not publish any advertisement or enter into any agreement stating or suggesting or giving tacit impression that they are in any way responsible for the obligations of its group entities. <br>
12.	The risk management practices expected to be adopted by the Company while outsourcing to a related party (i.e. party within the Group / Conglomerate) would be identical to those specified above.
</p>
               
                                     <h6 style="margin-top:2%;">17.	Maintenance of Records</h6><br>
                                     <p>The records relating to all the activities outsourced shall be preserved centrally either at the registered office of the Company or such other location as may be approved by the Board, so that these records are readily accessible for review by the Board and Senior Management of the Company, as and when required. Such records shall be updated promptly by any person authorized by the Board and/or its committee and half yearly reviews shall be placed before the Committee.

</p>
              
                      
